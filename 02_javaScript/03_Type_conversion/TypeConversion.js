// String to Number

let age = "25";

console.log(Number(age));
console.log(typeof Number(age));

// String to Integer

let marks = "85";

console.log(parseInt(marks));

// String to Float

let salary = "50000.75";

console.log(parseFloat(salary));

// Number to String

let num = 100;

console.log(String(num));
console.log(typeof String(num));

// Number to Boolean

let value = 1;

console.log(Boolean(value));

// Boolean to Number

let isStudent = true;

console.log(Number(isStudent));